import UIKit

extension UIColor
{
    
    class func lightYellow() -> UIColor
    {
        return UIColor(red: 254/255, green: 219/255, blue: 93/255, alpha: 1)
    }
    
    class func pastelYellow() -> UIColor
    {
        return UIColor(red: 255/255, green: 240/255, blue: 185/255, alpha: 1)
    }
}

